//Tambem é possivel declarar funções dentro de variaveis
//são chamadas de function anonymous ou function expression
const sum = function(number1, number2/*Parametros da função*/) {
    console.log(number1 + number2);
};
/*******************/

//rodando a função
//running function
sum(2, 3); 

